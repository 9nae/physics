function chapterShortTitle(title) {
  return title.replace(/^12\.3\.\d+\s*/, "");
}

function defaultSoundDemo(slideNumber) {
  if (slideNumber >= 43 && slideNumber <= 60) return "beat";
  if (slideNumber >= 61 && slideNumber <= 73) return "doppler";
  if (slideNumber >= 74 && slideNumber <= 79) return "shock";
  if (slideNumber >= 17 && slideNumber <= 42) return "resonance";
  return "tone";
}

function buildTeacherScript(chapter, slide, enhancement) {
  if (enhancement.teacherScript) return enhancement.teacherScript;
  const points = slide.points.map(stripMarkup).join(" ");
  return `ฉากนี้อยู่ในหัวข้อ ${chapterShortTitle(chapter.title)} เรื่อง ${slide.title} ครูอยากให้เราจับใจความทีละส่วนดังนี้ ${points} ถ้าอ่านแล้วให้ถามตัวเองว่า ข้อความนี้กำลังบอกเงื่อนไขของปรากฏการณ์ สูตรที่ใช้ หรือผลจากการทดลอง เพราะสามอย่างนี้คือแกนของบทเรื่องเสียง`;
}

function buildLessonScenes() {
  return lessonChapters.flatMap((chapter) =>
    chapter.slides.map((slide) => {
      const enhancement = sceneEnhancements[slide.n] || {};
      const formula = enhancement.formula || [];
      return {
        id: `scene-${slide.n}`,
        slideNumber: slide.n,
        chapterId: chapter.id,
        chapterTitle: chapter.title,
        chapterRange: chapter.range,
        title: slide.title,
        points: slide.points,
        teacherScript: buildTeacherScript(chapter, slide, enhancement),
        keyIdea: enhancement.keyIdea || stripMarkup(slide.points[0] || ""),
        formula,
        example: enhancement.example || "",
        quiz: enhancement.quiz || null,
        visual: enhancement.visual || null,
        media: enhancement.media || slideMediaMap[slide.n] || [],
        experiment: enhancement.experiment || "",
        soundDemo: enhancement.soundDemo || defaultSoundDemo(slide.n),
      };
    }),
  );
}

const lessonScenes = buildLessonScenes();
window.lessonChapters = lessonChapters;
window.lessonScenes = lessonScenes;

function renderLessonContent() {
  const root = document.getElementById("lessonContent");
  if (!root) return;

  const quickLinks = lessonChapters
    .map(
      (chapter) =>
        `<a href="#${chapter.id}"><span>${chapter.range}</span>${chapter.title}</a>`,
    )
    .join("");

  const chapters = lessonChapters
    .map((chapter) => {
      const slides = chapter.slides
        .map((slide) => {
          const scene = lessonScenes.find((item) => item.slideNumber === slide.n);
          const media = scene?.media?.length ? scene.media : scene?.visual?.src ? [scene.visual.src] : [];
          return `
            <article class="slide-card">
              <div class="slide-kicker">ช่วงที่ ${slide.n}</div>
              <h4>${slide.title}</h4>
              ${scene?.teacherScript ? `<p class="slide-teacher-text">${scene.teacherScript}</p>` : ""}
              ${scene?.keyIdea ? `<p class="slide-key-idea"><strong>ใจความสำคัญ:</strong> ${scene.keyIdea}</p>` : ""}
              ${
                scene?.formula?.length
                  ? `<div class="slide-formulas">${scene.formula.map((item) => `<span>${item}</span>`).join("")}</div>`
                  : ""
              }
              ${
                media.length
                  ? `<div class="slide-media-grid">${media
                      .map((src) => `<figure><img src="${src}" alt="สื่อประกอบเรื่อง ${slide.title}" /></figure>`)
                      .join("")}</div>`
                  : ""
              }
              <details class="slide-more">
                <summary>อ่านสาระย่อย</summary>
                <ul>
                  ${slide.points.map((point) => `<li>${point}</li>`).join("")}
                </ul>
              </details>
              ${
                scene?.quiz
                  ? `<p class="slide-quiz-note"><strong>คำถาม:</strong> ${scene.quiz.prompt}<br /><strong>เฉลย:</strong> ${scene.quiz.explanation}</p>`
                  : ""
              }
            </article>
          `;
        })
        .join("");

      return `
        <section id="${chapter.id}" class="chapter-block">
          <div class="chapter-intro">
            <p class="eyebrow">${chapter.range}</p>
            <h3>${chapter.title}</h3>
            <p>${chapter.intro}</p>
          </div>
          <div class="slide-list">${slides}</div>
        </section>
      `;
    })
    .join("");

  root.innerHTML = `
    <div class="content-jump">${quickLinks}</div>
    ${chapters}
  `;
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", renderLessonContent);
} else {
  renderLessonContent();
}
